                     c.lower() == 'e' or c.lower() == 'é' or \
                     c.lower() == 'i' or c.lower() == 'í' or \
                     c.lower() == 'o' or c.lower() == 'ó' or \
                     c.lower() == 'u' or c.lower() == 'ú'
